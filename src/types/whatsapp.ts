/**
 * Result shape returned by AI intent detection inside ai-responder.
 */
export interface IntentResult {
  /**
   * Primary intent category:
   * - 'inquiry'    — asking about a service/product
   * - 'pricing'    — asking about cost / pricing
   * - 'demo'       — requesting a demo or trial
   * - 'complaint'  — expressing dissatisfaction
   * - 'optout'     — asking to stop receiving messages
   * - 'greeting'   — general greeting / small talk
   * - 'unknown'    — could not be determined
   */
  intent: 'inquiry' | 'pricing' | 'demo' | 'complaint' | 'optout' | 'greeting' | 'unknown';
  /** Confidence score 0–1 */
  confidence: number;
  /** Suggested next action for the agent or automation */
  suggestedAction: 'reply' | 'schedule_call' | 'send_brochure' | 'escalate' | 'unsubscribe';
}

/**
 * Lead-scoring event logged when contact engagement changes.
 */
export interface LeadScoringEvent {
  /** The type of engagement that occurred */
  event:
    | 'message_received'   // +10
    | 'replied'            // +20
    | 'link_clicked'       // +15
    | 'demo_requested'     // +25
    | 'phone_call'         // +30
    | 'unsubscribed'       // −50
    | 'no_reply_7d';       // −5
  /** Point delta (positive or negative) */
  delta: number;
  /** ISO 8601 timestamp */
  occurredAt: string;
}

