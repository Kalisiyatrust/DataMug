/**
 * Aggregated analytics snapshot for the WhatsApp marketing system.
 * Computed on-demand from the underlying JSON stores.
 */
export interface WhatsAppAnalytics {
  /** Total number of contacts across all brands */
  totalContacts: number;
  /** Conversations with at least one message in the last 7 days */
  activeConversations: number;
  /** Cumulative outbound message count */
  messagesSent: number;
  /** Cumulative inbound message count */
  messagesReceived: number;
  /** Fraction of sent messages confirmed delivered (0–1) */
  deliveryRate: number;
  /** Fraction of delivered messages confirmed read (0–1) */
  readRate: number;
  /** Fraction of delivered messages that received a reply (0–1) */
  replyRate: number;
  /** Number of campaigns with status 'completed' or 'running' */
  campaignsSent: number;
  /** Most-used contact tags, sorted descending by count */
  topTags: { tag: string; count: number }[];
  /** Contact count per funnel stage */
  stageDistribution: { stage: string; count: number }[];
  /** Per-brand breakdown of contacts and message volume */
  brandBreakdown: { brand: string; contacts: number; messages: number }[];
}

