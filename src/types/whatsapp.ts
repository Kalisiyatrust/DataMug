// ─────────────────────────────────────────────────────────────────────────────
// Brand
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Configuration for a single brand that owns a WhatsApp number.
 * Drives outbound message routing and AI persona.
 */
export interface BrandConfig {
  /** Stable identifier — used as FK throughout the system */
  id: 'kalisiya' | 'eloi' | 'datamug';
  /** Human-readable display name */
  name: string;
  /** Short description shown in the UI */
  description: string;
  /** E.164-formatted WhatsApp number (e.g. +917794911850) */
  whatsappNumber: string;
  /** Full system prompt passed to the AI responder */
  systemPrompt: string;
  /** List of products/services the brand offers */
  services: string[];
  /** Description of the brand's target audience */
  targetAudience: string;
  /** Desired conversational tone for AI responses */
  tone: string;
}

// ─────────────────────────────────────────────────────────────────────────────
// Contact / Lead
// ─────────────────────────────────────────────────────────────────────────────

/**
 * A contact (lead) stored in the CRM.
 * One contact can belong to only one brand at a time.
 */
export interface Contact {
  /** UUID v4 */
  id: string;
  /** E.164 phone number */
  phone: string;
  /** Full name */
  name: string;
  /** Optional email address */
  email?: string;
  /** Optional company/organisation name */
  company?: string;
  /** Job title or role */
  role?: string;
  /** Which brand this contact belongs to */
  brand: BrandConfig['id'];
  /** Free-form segmentation tags (e.g. "donor", "enterprise", "trial") */
  tags: string[];
  /**
   * Engagement-based lead score, 0–100.
   * Automatically incremented by CRM engagement events.
   */
  leadScore: number;
  /** Funnel stage */
  stage: 'new' | 'contacted' | 'engaged' | 'qualified' | 'converted' | 'lost';
  /** How the contact entered the system */
  source: 'import' | 'manual' | 'inbound' | 'campaign';
  /** Free-form internal notes */
  notes: string;
  /** ISO 8601 timestamp of the last outbound or inbound message */
  lastContacted?: string;
  /** ISO 8601 creation timestamp */
  createdAt: string;
  /** ISO 8601 last-updated timestamp */
  updatedAt: string;
}

// ─────────────────────────────────────────────────────────────────────────────
// Conversation & Messages
// ─────────────────────────────────────────────────────────────────────────────

/**
 * A single WhatsApp message — either inbound (from the contact)
 * or outbound (sent by the system or a human agent).
 */
export interface Message {
  /** UUID v4 */
  id: string;
  /** The contact this message belongs to */
  contactId: string;
  /** Whether the message was received from or sent to the contact */
  direction: 'inbound' | 'outbound';
  /** Plain-text or template-rendered message body */
  body: string;
  /** Optional media attachment URL */
  mediaUrl?: string;
  /** Twilio delivery state */
  status: 'queued' | 'sent' | 'delivered' | 'read' | 'failed';
  /** Twilio Message SID (populated once sent) */
  twilioSid?: string;
  /** Which brand's number was used */
  brand: BrandConfig['id'];
  /** True when the body was generated by the AI responder */
  isAiGenerated: boolean;
  /** ISO 8601 timestamp */
  timestamp: string;
}

/**
 * Aggregated view of all messages between the system and a single contact.
 */
export interface Conversation {
  /** FK to Contact.id */
  contactId: string;
  /** Chronologically ordered message array */
  messages: Message[];
  /** Shortcut to the most recent message */
  lastMessage?: Message;
  /** Count of inbound messages not yet read by an agent */
  unreadCount: number;
  /** Whether the conversation is open (not archived/resolved) */
  isActive: boolean;
}

// ─────────────────────────────────────────────────────────────────────────────
// Templates & Campaigns
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Reusable message template.
 * Supports Handlebars-style placeholders: {{name}}, {{company}},
 * {{service}}, {{brand}}.
 */
export interface MessageTemplate {
  /** UUID v4 */
  id: string;
  /** Human-readable label */
  name: string;
  /**
   * Message body with optional {{placeholder}} tokens.
   * Rendered at send time by the campaign engine.
   */
  body: string;
  /** Owning brand */
  brand: BrandConfig['id'];
  /** Functional category for filtering / analytics */
  category: 'welcome' | 'followup' | 'promotion' | 'newsletter' | 'custom';
  /** ISO 8601 creation timestamp */
  createdAt: string;
}

/**
 * A bulk-send campaign targeting a subset of contacts via a template.
 */
export interface Campaign {
  /** UUID v4 */
  id: string;
  /** Human-readable campaign name */
  name: string;
  /** Owning brand */
  brand: BrandConfig['id'];
  /** Which template to send */
  templateId: string;
  /** Lifecycle status */
  status: 'draft' | 'scheduled' | 'running' | 'paused' | 'completed';
  /**
   * Only contacts with at least one of these tags are included.
   * Empty array = all contacts for the brand.
   */
  targetTags: string[];
  /**
   * Only contacts in these funnel stages are included.
   * Empty array = all stages.
   */
  targetStages: Contact['stage'][];
  /** ISO 8601 — when to start sending (null = send immediately) */
  scheduledAt?: string;
  /** Running total of messages sent */
  sentCount: number;
  /** Running total of messages confirmed delivered */
  deliveredCount: number;
  /** Running total of messages confirmed read */
  readCount: number;
  /** Running total of contacts who replied */
  repliedCount: number;
  /** ISO 8601 creation timestamp */
  createdAt: string;
  /** ISO 8601 last-updated timestamp */
  updatedAt: string;
}

// ─────────────────────────────────────────────────────────────────────────────
// Drip Sequences
// ─────────────────────────────────────────────────────────────────────────────

/**
 * One step in an automated drip sequence.
 */
export interface DripStep {
  /** UUID v4 */
  id: string;
  /** Template to send at this step */
  templateId: string;
  /** Days to wait after the previous step (or enrolment for step 1) */
  delayDays: number;
  /**
   * Conditional trigger:
   * - 'no_reply'  — only advance if the contact has NOT replied
   * - 'replied'   — only advance if the contact HAS replied
   * - 'any'       — always advance (default)
   */
  condition?: 'no_reply' | 'replied' | 'any';
}

/**
 * An ordered collection of drip steps that can be enrolled to contacts.
 */
export interface DripSequence {
  /** UUID v4 */
  id: string;
  /** Human-readable sequence name */
  name: string;
  /** Owning brand */
  brand: BrandConfig['id'];
  /** Ordered array of steps */
  steps: DripStep[];
  /** Whether new contacts can be enrolled */
  isActive: boolean;
  /** Contact IDs currently enrolled in this sequence */
  enrolledContacts: string[];
  /** ISO 8601 creation timestamp */
  createdAt: string;
}

/**
 * Tracks an individual contact's progress through a drip sequence.
 */
export interface DripEnrollment {
  /** UUID v4 */
  id: string;
  /** FK to DripSequence.id */
  sequenceId: string;
  /** FK to Contact.id */
  contactId: string;
  /** Zero-based index of the next step to execute */
  currentStepIndex: number;
  /** ISO 8601 — when to send the next step */
  nextSendAt: string;
  /** Whether this enrollment is still active */
  isActive: boolean;
  /** ISO 8601 enrolment timestamp */
  enrolledAt: string;
  /** ISO 8601 completion/exit timestamp */
  completedAt?: string;
}

